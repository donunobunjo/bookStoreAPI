    {
        $review = Review::firstOrCreate(
            [
              'user_id' => $request->user()->id,
              'book_id' => $request->$book->id,
            ],
            [
                'review' => $request->review,
                'comment' => $request->comment
            ]
          );
    
          return new ReviewResource($review);
    }

